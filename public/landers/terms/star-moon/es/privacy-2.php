<b>2. ¿Qué datos personales recopilamos de usted?</b>
<br><br>
2.1 Sólo recogemos sus Datos Personales cuando usted nos los facilita de forma directa y efectiva para obtener nuestros servicios y/o productos, resolver sus consultas y/o recibir boletines electrónicos y/o correos electrónicos promocionales relacionados con productos, eventos, noticias y servicios, tal y como se describe específicamente en el apartado 3.1 de la presente Política de Privacidad.
<br><br>
2.2 En este sentido, recabamos directamente de usted los siguientes Datos Personales:
<br><br>
<p class="px-3">a.  Datos Personales: nombre completo, apellidos, sexo, signo zodiacal, país de residencia y fecha de nacimiento;
    <br><br>
    b.  Datos de contacto: dirección de correo electrónico y, en algunos casos concretos, su número de teléfono; y
    <br><br>
    c.  Datos de pago: datos bancarios cuando desee adquirir directamente productos y/o servicios. </p>
<br><br>
2.3 En algunos casos, es posible que recopilemos información automáticamente. Encontrará más información sobre nuestro uso de Cookies y tecnologías de rastreo en nuestra Política de Cookies</a>.
