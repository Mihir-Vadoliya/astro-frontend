    f.  Si usted lo ha aceptado expresamente, enviarle boletines electrónicos y/o correos electrónicos promocionales relacionados con productos, eventos, noticias y servicios ofrecidos por AstroVeo;
    <br><br>
    g.  Para cualquier otro fin que se le haya comunicado en el momento en que nos proporcionó sus Datos Personales, en la medida en que nos haya otorgado su consentimiento previo para ese tratamiento concreto; y
    <br><br>
    h.  Para garantizar un nivel óptimo de cumplimiento de las obligaciones legales aplicables a las que AstroVeo está sujeto y para cooperar con los reguladores y las autoridades policiales cuando sea necesario.</p>
