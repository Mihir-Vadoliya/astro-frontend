<br><br>
3.1 Procesamos sus Datos Personales para los siguientes fines:
<br><br>
<p class="px-3">a.  Permitirle adquirir nuestros servicios y/o productos;
    <br><br>
    b.  Para proporcionarle nuestros servicios de astrofísica personalizados, como la exploración de su horóscopo diario, la lectura del tarot en línea, conferencias de astrofísica, etc;
    <br><br>
    c.  Gestionar sus consultas, preguntas y solicitudes;
    <br><br>
    d.  Para gestionar y proteger nuestro sitio web;
    <br><br>
    e.  Para comprender cómo utiliza nuestros servicios y permitirnos mejorar y seguir desarrollando la funcionalidad, el rendimiento y la asistencia disponibles en nuestro sitio web, lo que puede implicar el suministro de información estadística anonimizada sobre nuestros visitantes (sin embargo, sin que esto se utilice para identificar a ningún usuario individual);
