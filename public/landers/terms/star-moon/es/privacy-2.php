<b>4.  ¿Cómo compartimos sus Datos Personales y con quién?</b>
<br><br>
4.1 AstroVeo podrá compartir sus Datos Personales con terceros en las siguientes circunstancias:
<br><br>
<p class="px-3">a.  Proveedores de servicios y socios comerciales: Nuestros empleados, proveedores de servicios y socios comerciales que realizan, entre otros, el procesamiento seguro de pagos, el mantenimiento del sitio web y otros servicios informáticos, marketing y comunicación, contabilidad, finanzas, legales y de auditoría y otras operaciones comerciales necesarias para que AstroVeo pueda proporcionar los productos y/o servicios que usted ha solicitado. Por ejemplo, para la prestación de nuestros servicios, recibirán sus datos personales clarividentes profesionales, lectores de tarot y expertos en horóscopos que colaboran con AstroVeo y que cumplen todas las obligaciones de protección de datos y confidencialidad; Harvest Direct Ltd, IWS International Web Services AG y Quality Online Services LLC.
    <br><br>
    b.  Organismos encargados de hacer cumplir la ley, tribunales, reguladores, autoridad gubernamental u otro tercero con autoridad imperativa: Podemos compartir sus datos personales con estas partes cuando creamos que es necesario para cumplir con una obligación legal o reglamentaria, o de otro modo para proteger nuestros derechos o los derechos de cualquier tercero; y
    <br><br>
    c.  Terceros: Compartiremos sus datos personales sólo si es estrictamente necesario debido a un requisito legal y/o a terceros cuando usted haya dado su consentimiento para enviarles sus datos personales para la recepción de ofertas de terceros proveedores; Selecciones clave. Para evitar cualquier duda, AstroVeo le informa de que ha tomado todas las medidas legales necesarias en relación con estos terceros para cumplir con la normativa aplicable en materia de protección de datos, tales como el análisis del destinatario de sus datos personales, su jurisdicción aplicable, así como la firma de los correspondientes acuerdos de protección y transferencia de datos cuando sea necesario.</p>
<br><br>
<b>5.  ¿Cuáles son las bases jurídicas para el tratamiento de sus Datos Personales?</b>
<br><br>
5.1 AstroVeo le informa que cumplirá en todo momento con el contenido del Reglamento (UE) 2016/679 del Parlamento Europeo y del Consejo, de 27 de abril de 2016, relativo a la protección de las personas físicas en lo que respecta al tratamiento de datos personales y a la libre circulación de estos datos y por el que se deroga la Directiva 95/46/E, en adelante "GDPR". En particular, sus Datos Personales serán procesados y tratados por AstroVeo de conformidad con las siguientes cláusulas y apartados incluidos en dicho Reglamento:
<br><br>
<p class="px-3">a.  En caso de que usted adquiera uno de nuestros productos o contrate uno de nuestros servicios, AstroVeo tratará sus Datos Personales de conformidad con la Cláusula 6.1, Apartado "b" incluido en el GDPR. La razón por la que este tratamiento se basará en esta Cláusula y Sección es porque usted estará entablando una relación comercial de compra o contratación de servicios con AstroVeo. Esta relación comercial se regirá por el contenido incluido en nuestros Términos y Condiciones. Por lo tanto, le recomendamos encarecidamente que revise el contenido de dicho acuerdo.
    <br><br>
    b.  AstroVeo le informa que, con el fin de cumplir en todo momento con la Cláusula 6.1, Apartado "a" del GDPR, solicitamos su consentimiento expreso y libre con la finalidad de:
    <br><br>
    i.  Enviar nuestros boletines y/o correos electrónicos promocionales relacionados con productos, eventos, noticias y servicios; y/o
    <br><br>
    ii. Poder facilitar sus datos a terceros.</p>
<br><br>
<b>6.  ¿Cuáles son sus derechos en relación con sus Datos Personales?</b>
<br><br>
6.1 AstroVeo le informa de que el GDPR y cualquier otra normativa aplicable en materia de protección de datos, que pueda serle de aplicación, le reconoce y garantiza la posibilidad de ejercer y hacer valer los siguientes derechos:
<br><br>
<p class="px-3">a.  Conocer cómo estamos tratando sus Datos Personales, así como poder acceder a los mismos;
    <br><br>
    b.  A solicitar la rectificación de los Datos Personales inexactos o incompletos que AstroVeo pueda tener sobre usted;
    <br><br>
    c.  A solicitar la supresión de sus Datos Personales cuando éstos ya no sean necesarios para los fines iniciales para los que fueron recogidos inicialmente, de conformidad con la legislación aplicable;
    <br><br>
    d.  Limitar y/o oponerse al tratamiento de sus Datos Personales. Para evitar dudas, AstroVeo podrá conservar sus Datos Personales con el fin de ejercer nuestros derechos legales;
    <br><br>
    e.  A solicitar la portabilidad de sus datos personales, lo que le permitirá obtener y utilizar los datos personales en un formato electrónico utilizable para sus propios fines y en diferentes servicios, sin impedimentos para su utilización, incluida la transmisión a otro tercero; y
    <br><br>
    f.  Retirar en cualquier momento el consentimiento que haya otorgado para un tratamiento específico.</p>
<br><br>
6.2 AstroVeo le recomienda encarecidamente que se ponga en contacto con nosotros para actualizar o corregir su información si ésta cambia o si los datos personales que tenemos sobre usted son inexactos. En caso necesario, nos pondremos en contacto con usted si necesitamos información adicional para satisfacer sus solicitudes.
<br><br>
<b>7.  ¿Cómo protegemos sus datos personales? </b>
<br><br>
7.1 Aplicamos medidas técnicas y organizativas para garantizar un nivel de seguridad adecuado al riesgo que corren los datos personales que tratamos. Estas medidas pretenden garantizar la integridad y confidencialidad permanentes de los datos personales. Evaluamos periódicamente estas medidas para garantizar la seguridad del tratamiento.
<br><br>
7.2 Además, como ya se ha indicado en esta Política de Privacidad, AstroVeo toma todas las medidas legales necesarias en relación con terceros que puedan recibir sus datos personales para cumplir con la Normativa de Protección de Datos aplicable, como por ejemplo analizando el destinatario de sus datos personales, su jurisdicción aplicable y firmando los acuerdos de Protección de Datos y Transferencia de Datos pertinentes cuando sea necesario.
<br><br>
<b>8.  ¿Durante cuánto tiempo conservamos sus Datos Personales? </b>
<br><br>
8.1 Conservaremos sus Datos Personales mientras mantengamos una relación con usted. Una vez finalizada nuestra relación, conservaremos sus Datos Personales durante un período de tiempo que nos permita:
<br><br>
- Mantener registros comerciales con fines de análisis y/o auditoría;<br>
- Cumplir los requisitos legales de conservación de registros;<br>
- Defender o presentar cualquier reclamación legal existente o potencial;<br>
- Atender cualquier reclamación relativa a los servicios.<br><br>
8.2 Eliminaremos sus Datos Personales cuando ya no sean necesarios para estos fines. Si hay alguna información que, por razones técnicas, no podemos eliminar por completo de nuestros sistemas, tomaremos las medidas adecuadas para evitar cualquier tratamiento o uso posterior de los datos.
