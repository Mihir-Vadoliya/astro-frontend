<b>Etica 1: Rispetto per tutti</b>
<br><br>
I prodotti e servizi offerti da Astro Rolla non sono rivolti a persone:
<br><br>
- In difficoltà finanziarie<br>
- Minori <br>
- Che non sono in pieno possesso delle proprie facoltà mentali
<br><br>
<b>Etica 2: Onestà, Integrità e Giustizia</b>
<br><br>
Astro Rolla e il suo personale sono pienamente impegnati a rispettare i principi di onestà, integrità e correttezza nella fornitura dei propri prodotti e servizi ai propri clienti.<br><br>
Tutti i dipendenti della Società assicurano che le transazioni commerciali, le richieste di servizi, gli acquisti o le assunzioni di personale siano gestite in modo aperto, equo e imparziale.<br><br>
Questa carta definisce gli standard di condotta attesi da tutti i dipendenti, nonché la politica aziendale sull'accettazione dei benefici e sulla gestione dei conflitti di interesse nell'esercizio delle funzioni d'ufficio.<br><br>
 La presente Carta si applica anche ai dipendenti a tempo determinato e part-time assunti dalla Società.
