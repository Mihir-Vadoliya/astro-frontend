<b>Ethics 1: Respect for all</b>
<br><br>
The products and services offered by Abstract Astro do not target people:
<br><br>
- In financial difficulties<br>
- Minors <br>
- Who are not in full possession of their mental faculties
<br><br>
<b>Ethics 2: Honesty, integrity and justice</b>
<br>
Abstract Astro and its staff are fully committed to the principles of honesty, integrity and fairness in the delivery of their products and services to their customers.
<br><br>
All company employees ensure that commercial transactions, requests for services, purchases or hiring of personnel are handled in an open, fair and impartial manner.
<br><br>
This charter sets out the standards of conduct expected of all staff members, as well as the company's policy on accepting benefits and managing conflicts of interest in the performance of official duties.
<br><br>
This Charter also applies to temporary and part-time staff employed by the Company.
