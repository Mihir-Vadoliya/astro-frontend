<h2>Ethical rules </h2>
<p><strong>Principle 1: Respect for the person</strong></p>
<br>
<p>The products and services provide by Astrofysica are not directed at people:</p>
<p>- In financial hardship<br>
- Minors <br>
- Who are not in full possession of their mental faculties
</p><br><br>
<p><strong>Principle 2: Honesty, integrity and fairness</strong></p><br>
<p>Astrofysica and its staff are fully committed to the principles of honesty, integrity and fairness in the delivery of their products and services to their clients.</p><br>
<p>All Company employees ensure that commercial transactions, requests for services, purchases or hiring of staff are handled in an open, fair and impartial manner.</p><br>
<p>This Charter sets out the standards of conduct expected of all staff as well as the Company policy on accepting benefits and managing conflict of interest in the course of official duties.</p><br>
<p>This Charter shall also apply to temporary and part-time staff employed by the Company.</p>